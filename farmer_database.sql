CREATE TABLE FERTILISERS(
FERTILISER_ID INT PRIMARY KEY,
CROP_NAME VARCHAR(20),
COMPANY VARCHAR(15),    
PRICE INT,
CROP_ID INT,
FOREIGN KEY (CROP_ID) REFERENCES CROP(CROP_ID)
);

CREATE TABLE PESTICIDES(
PESTICIDES_ID INT PRIMARY KEY,
CROP_NAME VARCHAR(20),
COMPANY VARCHAR(15),    
PRICE INT,
CROP_ID INT,
FOREIGN KEY (CROP_ID) REFERENCES CROP(CROP_ID)
);

CREATE TABLE AGROSTORE(
STORE_NAME VARCHAR(20) PRIMARY KEY,
CROP_NAME VARCHAR(20),
SEED_ID INT,
FERTILISER_ID INT,
PESTICIDES_ID INT,
CROP_ID INT,
FOREIGN KEY (CROP_ID) REFERENCES CROP(CROP_ID),
FOREIGN KEY (FERTILISER_ID) REFERENCES FERTILISERS(FERTILISER_ID),
FOREIGN KEY (PESTICIDES_ID) REFERENCES PESTICIDES(PESTICIDES_ID),
FOREIGN KEY (SEED_ID) REFERENCES SEEDS(SEED_ID)
);

CREATE TABLE BLOG(
BLOG_ID INT PRIMARY KEY,
timestamp datetime,
content VARCHAR(120),
AADHAR_NUMBER INT,
FOREIGN KEY (AADHAR_NUMBER) REFERENCES FARMER(AADHAR_NUMBER)
);



CREATE TABLE QUERYH(
QUERY_ID INT PRIMARY KEY,
content VARCHAR(120),
AADHAR_NUMBER INT,
FOREIGN KEY (AADHAR_NUMBER) REFERENCES FARMER(AADHAR_NUMBER)
);



CREATE TABLE EXPERT(
AADHAR_NUMBER INT PRIMARY KEY,
NAME VARCHAR(20),
QUALIFICATION VARCHAR(20),
QUERY_ID INT,
FOREIGN KEY (QUERY_ID) REFERENCES QUERYH(QUERY_ID)
);



CREATE TABLE INVENTORY(
INVENTORY_ID INT PRIMARY KEY,
AGROSTORE VARCHAR(20),
FOREIGN KEY (AGROSTORE) REFERENCES AGROSTORE(STORE_NAME)
);





