CREATE TABLE CROP (
    CROP_ID INT PRIMARY KEY,
    CROP_NAME VARCHAR(20),
    CROP_SEASON VARCHAR(20),
    AADHAR_NUMBER INT,
    FOREIGN KEY (AADHAR_NUMBER) REFERENCES FARMER(AADHAR_NUMBER)
);


CREATE TABLE SEEDS (
    SEED_ID INT PRIMARY KEY,
    CROP_ID INT,
    COMPANY VARCHAR(30),
    PRICE INT,
    FOREIGN KEY (CROP_ID) REFERENCES CROP(CROP_ID)
);

CREATE TABLE FERTILISERS (
    FERTILISER_ID INT PRIMARY KEY,
    CROP_ID INT,
    COMPANY VARCHAR(15),
    PRICE INT,
    FOREIGN KEY (CROP_ID) REFERENCES CROP(CROP_ID)
);


CREATE TABLE PESTICIDES (
    PESTICIDES_ID INT PRIMARY KEY,
    CROP_ID INT,
    COMPANY VARCHAR(15),
    PRICE INT,
    FOREIGN KEY (CROP_ID) REFERENCES CROP(CROP_ID)
);

CREATE TABLE LAND (
    LAND_ID INT PRIMARY KEY,
    LAND_OWNER VARCHAR(20),
    AADHAR_NUMBER INT,
    CROP_ID INT,
    FOREIGN KEY (AADHAR_NUMBER) REFERENCES FARMER(AADHAR_NUMBER),
    FOREIGN KEY (CROP_ID) REFERENCES CROP(CROP_ID)
);

CREATE TABLE AGROSTORE (
    STORE_NAME VARCHAR(20) PRIMARY KEY,
    LOCATION VARCHAR(50)
);

CREATE TABLE AGROSTORE_SEEDS (
    STORE_NAME VARCHAR(20),
    SEED_ID INT,
    PRIMARY KEY (STORE_NAME, SEED_ID),
    FOREIGN KEY (STORE_NAME) REFERENCES AGROSTORE(STORE_NAME),
    FOREIGN KEY (SEED_ID) REFERENCES SEEDS(SEED_ID)
);

CREATE TABLE AGROSTORE_FERTILISERS (
    STORE_NAME VARCHAR(20),
    FERTILISER_ID INT,
    PRIMARY KEY (STORE_NAME, FERTILISER_ID),
    FOREIGN KEY (STORE_NAME) REFERENCES AGROSTORE(STORE_NAME),
    FOREIGN KEY (FERTILISER_ID) REFERENCES FERTILISERS(FERTILISER_ID)
);


CREATE TABLE AGROSTORE_PESTICIDES (
    STORE_NAME VARCHAR(20),
    PESTICIDES_ID INT,
    PRIMARY KEY (STORE_NAME, PESTICIDES_ID),
    FOREIGN KEY (STORE_NAME) REFERENCES AGROSTORE(STORE_NAME),
    FOREIGN KEY (PESTICIDES_ID) REFERENCES PESTICIDES(PESTICIDES_ID)
);


